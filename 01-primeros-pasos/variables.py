# Variables in order to store data in Python
# A variable is a named location in memory that can store a value.
x = "Esta es una variable"
print(x) # Imprime el valor de la variable x
x = "Ahora tiene un nuevo valor"
print(x) # Imprime el nuevo valor de la variable x


# Case sensitivity in Python
X = "Esta es una variable diferente a x"
print(X) # Imprime el valor de la variable X

